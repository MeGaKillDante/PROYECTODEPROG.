    private void btInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInsertarActionPerformed
        // TODO add your handling code here:

        Cliente nuevo=new Cliente();
        nuevo.setDni(txtDNI.getText());
        nuevo.setNombre(txtNombre.getText());
        nuevo.setApellido(txtApellido.getText());
        nuevo.setTele(txtTele.getText());
        nuevo.setMes(combo.getSelectedItem().toString());
        nuevo.setDi(Integer.parseInt(txtInicio.getText()));
        nuevo.setDf(Integer.parseInt(txtFinal.getText()));
        if(rdEfectivo.isSelected())nuevo.setMetodo("Efectivo");
        if(rdTarjeta.isSelected())nuevo.setMetodo("Tarjeta");
        listaEmpleado.add(nuevo);
        limpiar();
        llenar();
    }//GEN-LAST:event_btInsertarActionPerformed

