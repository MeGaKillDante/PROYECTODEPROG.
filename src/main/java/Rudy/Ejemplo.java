    void cargarTabla(){
        modelo.addColumn("DNI");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDO");
        modelo.addColumn("TELEFONO");
        modelo.addColumn("MES DE ESTANCIA");
        modelo.addColumn("DIA DE INICIO");
        modelo.addColumn("DIA FINAL");
        modelo.addColumn("METODO DE PAGO");
        Tabla.setModel(modelo);
    }
    private void llenar(){
        for (int i=0;i<listaEmpleado.size();i++){
            modelo.addRow(new Object[]{listaEmpleado.get(i).getDni(),listaEmpleado.get(i).getNombre(),listaEmpleado.get(i).getApellido(),listaEmpleado.get(i).getTele(),listaEmpleado.get(i).getMes(),
            listaEmpleado.get(i).getDi(),listaEmpleado.get(i).getDf(),listaEmpleado.get(i).getMetodo()});}
    }
    private void limpiar(){
        int a=Tabla.getRowCount()-1;
        for (int i=a;i>=0;i--){
            modelo.removeRow(modelo.getRowCount()-1);
        }
    }


